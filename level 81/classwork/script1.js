// ორი რიცხვის ჯამი
export function sum(a, b) {
  return a + b;
}

// გაყოფა
export function divide(a, b) {
  return a / b;
}

// ნამრავლი
export function multiply(a, b) {
  return a * b;
}

// სხვაობა
export function substract(a, b) {
  return a - b;
}
